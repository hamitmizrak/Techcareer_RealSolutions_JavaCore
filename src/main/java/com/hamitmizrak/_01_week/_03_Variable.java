package com.hamitmizrak._01_week;

public class _03_Variable {


    public static void main(String[] args) {
        // Variables
        int number=44;
        System.out.println(number);

        // Değişkenler Sayı ile bitebilir
        int number23=23;
        System.out.println(number23);

        // Değişkenler underscore(_) veya $  ile başlayabilir
        int _number=11;
        System.out.println(_number);

        int $number=22;
        System.out.println($number);

        int telNumber=11122233;
        System.out.println(telNumber);

        int telNumber2=111_222_33;
        System.out.println(telNumber2);

        // _, sayı ve $ haricinde `-+& başlanmaz
        
    }
}
